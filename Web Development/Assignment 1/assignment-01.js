// Add Funtion of Counter1
function add1()
{
    let value = document.getElementById("counter1").innerHTML; //Let value be default set from HTML input
    value++; //Increment by 1 if '+' button is pressed
    document.getElementById("counter1").innerHTML = value; //Change default value to incremented value
}
// Subtract Funtion of Counter1
function subtract1()
{
    let value = document.getElementById("counter1").innerHTML; //Let value be default set from HTML input
    value--; //Decrement by 1 if '-' button is pressed
    document.getElementById("counter1").innerHTML = value; //Change default value to decremented value
}

